// Extra non-matching character for special chars:
const uint8_t bad_codepoint_8x8[] = {
  0x00, 0x7e, 0x42, 0x42, 0x42, 0x42, 0x7e, 0x00 };

// Returns a pointer to the bitmap for the given character.
// Each bitmap is 8x8 bits (8 bytes), each byte corresponding to a single row of
// pixels.
// Returns a default bitmap if the character is out-of-range.
const uint8_t *font_char(char ch) {
  if (ch >= 0x20 && ch <= 0x7e) {
    return &font_8x8[8 * (ch-0x20)];
  }
  return bad_codepoint_8x8;
}
